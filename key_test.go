package memkvdb

import (
	"testing"

	"github.com/stretchr/testify/assert"
)

func TestHash(t *testing.T) {
	assert := assert.New(t)

	res, err := hash(nil)
	assert.Equal(DBKey{}, res)
	assert.Equal(ErrEmptyKey, err)

	res, err = hash([]byte{})
	assert.Equal(DBKey{}, res)
	assert.Equal(ErrEmptyKey, err)

	res, err = hash([]byte{0x00})
	assert.Equal(DBKey{0x93, 0xb8, 0x85, 0xad, 0xfe, 0xd, 0xa0, 0x89, 0xcd, 0xf6, 0x34, 0x90, 0x4f, 0xd5, 0x9f, 0x71}, res)
	assert.Nil(err)

	res, err = hash([]byte{0xff, 0x00, 0x00, 0x00, 0xf0, 0x1e, 0x0a, 0x99, 0xd4, 0x1d, 0x8c, 0xd9, 0x8f, 0x0, 0xb2, 0x4, 0xe9, 0x80, 0x9, 0x98, 0xec, 0xf8, 0x42, 0x7e})
	assert.Equal(DBKey{0xec, 0x4d, 0xcc, 0xb4, 0x60, 0x14, 0x20, 0x1f, 0xd9, 0x4c, 0xb5, 0x7, 0x68, 0xfa, 0x7c, 0xfd}, res)
	assert.Nil(err)

}
